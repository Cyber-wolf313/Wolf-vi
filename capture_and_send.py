# Get Telegram Bot Token and Chat ID
TOKEN = input("Enter your Telegram Bot Token: ")
CHAT_ID = input("Enter your Telegram Chat ID: ")

bot = Bot(token=TOKEN)

def capture_photos(num_photos):
    file_paths = []
    for i in range(num_photos):
        file_path = f'/data/data/com.termux/files/home/photo_{i}.jpg'
        subprocess.run(['termux-camera-photo', file_path])
        file_paths.append(file_path)
        time.sleep(5)  # Wait for 5 seconds between captures
    return file_paths

def send_photos(file_paths):
    for file_path in file_paths:
        try:
            with open(file_path, 'rb') as photo:
                bot.send_photo(chat_id=CHAT_ID, photo=photo)
            print("Photo successfully sent to Telegram bot.")
        except NetworkError:
            print("Failed to send photo. Check your network connection.")
        time.sleep(1)  # Wait for 1 second between sends

def main():
    print("Capturing and sending 5 photos...")
    photo_paths = capture_photos(5)
    send_photos(photo_paths)
    for file_path in photo_paths:
        os.remove(file_path)
    print("Photos successfully sent to your Telegram bot.")

if __name__ == "__main__":
    main()
